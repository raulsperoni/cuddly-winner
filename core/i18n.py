def normalize_language(value: str | None) -> str:
    if not value:
        return DEFAULT_LANGUAGE
    value = value.lower()
    if value.startswith('es'):
        return 'es'
    return 'en'


def get_language_from_request(request) -> str:
    query_language = normalize_language(request.GET.get('lang'))
    if 'lang' in request.GET and query_language in SUPPORTED_LANGUAGES:
        return query_language
    cookie_language = normalize_language(request.COOKIES.get(LANGUAGE_COOKIE))
    if cookie_language in SUPPORTED_LANGUAGES:
        return cookie_language
    header_language = normalize_language(
        request.META.get('HTTP_ACCEPT_LANGUAGE', '').split(',')[0]
    )
    return header_language if header_language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(language: str, key: str, **kwargs: Any) -> str:
    text = TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(
        key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    )
    return text % kwargs if kwargs else text
